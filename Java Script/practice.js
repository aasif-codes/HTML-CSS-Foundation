 /* 
    # Js Variable naming Rules
    
 1. Variable name must be unique (cannot use same name in same scope)


 2. Numbers are allowed, but the name should NOT start with a number
 Correct: name1 Wrong: 1name


 3. Only "_" (underscore) and "$" (dollar sign) are allowed as special characters
 Other special characters like @, #, -, % are NOT allowed

 4. No spaces are allowed between letters or words
 Correct: userName
 Wrong: user name


 5. JavaScript is case-sensitive
 age, Age, and AGE are different variables


 6. Variable value can be changed if declared with "let"
 If declared with "const", the value cannot be changed */